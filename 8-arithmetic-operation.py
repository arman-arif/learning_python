# Arithmetic Operations

print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)
print(10 // 3)  # returns integer
print(10 % 3)   # returns reminder
print(10 ** 3)  # returns power

x = 10
# augmented / enhance assigment operator
x += 3  # x = x + 3
print(x)

x -= 7  # x = x - 7
print(x)

